// Generates the Runtime-owned bundled catalog from the reviewable
// SKILL.md sources under resources/bundled-skills/. The generated module embeds
// each body as a string literal so the shipped app has no runtime dependency on
// the resources directory (mirrors how the Office skills embed their bodies).
//
// Run after editing any resources/bundled-skills/*/SKILL.md:
//   node scripts/gen-bundled-skill-catalog.mjs
//
// A drift test (bundled-skill-catalog.test.ts) fails if the checked-in generated
// module does not match a fresh regeneration, so this never silently goes stale.

import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const sourcesDir = join(here, '..', 'resources', 'bundled-skills');
const outFile = join(here, '..', '..', '..', 'packages', 'runtime', 'src', 'bundled-skill-catalog.generated.ts');

const ID_RE = /^[A-Za-z0-9][A-Za-z0-9._-]{0,80}$/;

export function readBundledSkillSources(dir = sourcesDir) {
  const ids = readdirSync(dir, { withFileTypes: true })
    .filter((entry) => entry.isDirectory() && ID_RE.test(entry.name))
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b));

  return ids.map((id) => {
    const body = readFileSync(join(dir, id, 'SKILL.md'), 'utf8');
    if (!body.startsWith('---')) {
      throw new Error(`bundled skill ${id} is missing YAML front-matter`);
    }
    return { id, body };
  });
}

export function renderGeneratedModule(skills) {
  const quote = (value) => {
    const json = JSON.stringify(value);
    return `'${json.slice(1, -1).replaceAll("'", "\\'")}'`;
  };
  const entries = skills
    .map(
      (skill) => `  {
    id: ${quote(skill.id)},
    body: ${quote(skill.body)},
  },`,
    )
    .join('\n');
  return `// @generated by scripts/gen-bundled-skill-catalog.mjs — do not edit by hand.
// Source of truth: resources/bundled-skills/<id>/SKILL.md
// Regenerate: node scripts/gen-bundled-skill-catalog.mjs

export interface BundledSkillSource {
  id: string;
  body: string;
}

export const BUNDLED_REVERSE_ENGINEERED_SKILLS: ReadonlyArray<BundledSkillSource> = [
${entries}
];
`;
}

function main() {
  const skills = readBundledSkillSources();
  writeFileSync(outFile, renderGeneratedModule(skills), 'utf8');
  console.log(`[gen] wrote ${skills.length} bundled skills -> ${outFile}`);
}

if (process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1]) {
  main();
}
